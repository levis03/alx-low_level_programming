/**
 * -isalpha - checks for alphabetic character
 * @c: the character to be checked
 * Return: 1 if c is a letter, 0 otherwise
 */
int _isalpha(int c)
	  {   
		             if((c >= 'a' && c <= 'z') ||
					                   (c >= 'A' && c <= 'Z'))
				                           Return (1);
			                      else
						                           Return (0);
					         } 
